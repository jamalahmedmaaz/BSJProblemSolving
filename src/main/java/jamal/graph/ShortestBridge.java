    int[][] directions = new int[][]{{1, 0}, {0, 1}, {-1, 0}, {0, -1}};

    public int shortestBridge(int[][] A) {
        int r = A.length;
        int c = A[0].length;
        Queue<int[]> queue = new LinkedList();
        Queue<int[]> pqueue = new LinkedList();
        boolean[][] pvisited = new boolean[r][c];
        for (int i = 0; i < A.length; i++) {
            if (queue.isEmpty()) {
                for (int j = 0; j < A[i].length; j++) {
                    if (A[i][j] == 1) {
                        queue.add(new int[]{i, j});
                        // System.out.println("Queuue "+i+"  "+j);
                        break;
                    }
                }
            }
        }

        boolean[][] visited = new boolean[r][c];
        while (!queue.isEmpty()) {
            int[] cell = queue.poll();
            int x = cell[0];
            int y = cell[1];
            A[x][y] = 3;
            if (A[x][y] == 3 || pvisited[x][y]) {
                pqueue.add(cell);
                pvisited[x][y] = true;
            }
            visited[x][y] = true;
            for (int[] dir : directions) {
                int nx = x + dir[0];
                int ny = y + dir[1];
                if (nx < 0 || ny < 0 || nx >= r || ny >= c || visited[nx][ny] || A[nx][ny] == 0) {
                    continue;
                }
                if (A[nx][ny] == 1) {
                    // System.out.println("Adding to queue" +nx+"  "+ny);
                    queue.add(new int[]{nx, ny});
                }
            }
        }

        int min = Integer.MAX_VALUE;
        while (!pqueue.isEmpty()) {
            int[] cell = pqueue.poll();
            int x = cell[0];
            int y = cell[1];
            int m = cell[2];
            // System.out.println("Current min "+m);
            if (A[x][y] == 1) {
                min = Math.min(min, m);
                if (min == 1) {
                    return min;
                }
                break;
            }
            visited[x][y] = true;
            for (int[] dir : directions) {
                int nx = x + dir[0];
                int ny = y + dir[1];

                if (nx < r && ny < c && nx >= 0 && ny >= 0 && !visited[nx][ny] && A[nx][ny] != 3) {
                    pqueue.add(new int[]{nx, ny, m + 1});
                }

            }
        }
        return min - 1;
    }
